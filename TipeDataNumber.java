public class TipeDataNumber {
    public static void main(String[] args) {
        // Biangan Bulat
        byte tipeDataByte = 10;
        short tipeDataShort =  3276;
        int tipeDataInt = 10;
        long tipeDataLong = 10;

        // Bilangan Pecahan
        float tipeDataFloat = 4.4F;
        double tipeDataDouble = 5.5;

        // Sistem Bilangan
        int decimalInt = 20;
        int hexInt = 0xFF;
        int binDecimal = 0b0101;

        int sebuahBilangan = 1_000_0000;

        System.out.println(binDecimal);

    }
}


